package var;

public class BOOLEAN {
	public static void main(String[] args) {
		//boolean : 불리언
		//true(참) 또는 false(거짓)만 저장할 수 있는 작은 변수
		
		//변수제작법
		//타입 이름;
		boolean a;
		//boolean true; 이름에는 예약어를 사용할 수 없다
		a=true;
		System.out.println(a);
		
		boolean b=false;
		System.out.println(b);
		
		boolean c=true,d=true;
		System.out.println(c);
		
		//이름이 F인 불리언 타입에 변수를 만들어서 false를 입력하고
		//콘솔에 출력하시오
		boolean F;
		F=false;
		System.out.println(F);
		boolean T;
		T=true;
		System.out.println(T);
		
	}
}

package var;

public class INT {
	public static void main(String[] args) {
		//byte	: 바이트, 아주 작은 수가 저장될 수 있는 타입
		//short	: 숏, 작은 수가 저장될 수 있는 타입
		//int	: 인티저, 일상적인 수가 저장될 수 있는 타입 ***
		//long	: 롱, 아주 큰 수가 저장될 수 있는 타입
		
		byte a;
		a=127;
		System.out.println(a);
		
		short b;
		b=10000;
		
		int c;
		c=1000000;
		
		int a1bc;	//변수 이름의 첫문자는 숫자를 적으면 안된다
		
		long d=222222222222222L;
	}
}

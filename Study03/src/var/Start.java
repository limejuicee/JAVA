package var;

public class Start {
	public static void main(String[] args) {
		//변수 만드는 법
		//크기 이름;
		int a;
		int b;
		//변수에 데이터를 저장하는 방법
		a=1000;
		a=90000;
		//변수는 한번에 하나의 데이터만을 가질 수 있다.
		System.out.println(a);
		
		//변수 이름 만드는 규칙(변수 만들때 반드시 지킬것)
		//1. 숫자로 시작하지 말것
		//2. 예약어를 사용하지 말것
		//3. 대소문자 구별에 주의
		//4. 이미 사용중인 이름을 사용하지 말것
		//5. 특수문자는 $_이외에는 사용하지 말것
		//6. 띄어쓰기 쓰지 말것
		
		//변수의 타입 종류 (상자의 크기 및 규격)
		boolean b;	//불리언
		char c; 	//캐릭터
		byte B; 	//바이트
		short s; 	//숏
		int i;		//인티저
		long L; 	//롱
		float f; 	//플롯
		double d;	//더블
	}//main 끝
}//class 끝

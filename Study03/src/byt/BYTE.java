package byt;

public class BYTE {
	public static void main(String[] args) {
		//byte : 바이트
		//아주 작은 수 를 저장할 수 있는 규격
		//통상적인 프로그램 제작에는 사용할 수 없다
		
		byte a=-128;
		System.out.println(a);
		//바이트는 잘 쓰지 않음
		
		//short : 숏
		//작은 수를 저장할 수 있는 규격
		//숏도 잘 쓰지 않음
		short b=1000;
		System.out.println(b);
	}//main 끝

}//class 끝

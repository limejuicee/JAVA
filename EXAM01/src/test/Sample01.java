package test;

public class Sample01 {
	public static void main(String[] args) {
		 System.out.println("동해물과 백두산이 \n마르고 닳도록");
		 // \n \r
		 System.out.println("이름:이승구 나이:25 주소:서울시 노원구");
		 System.out.println("이름:김환 나이:41 주소:서울시 중구");
		 System.out.println("이름:이승구\t나이:25\t주소:서울시 노원구");
		 System.out.println("이름:김환\t나이:41\t주소:서울시 중구");
		 
		 System.out.println("문자를 하나만 적을 때는 \'안에 넣어야 한다");
		 System.out.println("명령에 쓰이는 문자를 그냥 출력시킬때는 앞에 \\를 붙인다");
	}
}
